interface EmojiPickerProps {
  onSelect: (emoji: string) => void;
}

export function EmojiPicker({ onSelect }: EmojiPickerProps) {
  const [open, setOpen] = useState(false);
  const [activeCategory, setActiveCategory] = useState(0);
  const [search, setSearch] = useState("");
  const containerRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    if (!open) return;
    function handleClick(e: MouseEvent) {
      if (containerRef.current && !containerRef.current.contains(e.target as Node)) {
        setOpen(false);
      }
    }
    document.addEventListener("mousedown", handleClick);
    return () => document.removeEventListener("mousedown", handleClick);
  }, [open]);

  const filteredEmojis = search.trim()
    ? EMOJI_CATEGORIES.flatMap((c) => c.emojis).filter((e) =>
        EMOJI_CATEGORIES.some(
          (c) =>
            c.emojis.includes(e) &&
            c.label.toLowerCase().includes(search.toLowerCase())
        )
      )
    : EMOJI_CATEGORIES[activeCategory].emojis;

  // Simple keyword search across all categories
  const displayEmojis = search.trim()
    ? EMOJI_CATEGORIES.flatMap((c) =>
        c.label.toLowerCase().includes(search.toLowerCase()) ? c.emojis : []
      )
    : EMOJI_CATEGORIES[activeCategory].emojis;

  return (
    <div ref={containerRef} className="relative">
      <button
        type="button"
        onMouseDown={(e) => {
          e.preventDefault();
          setOpen((v) => !v);
        }}
        className="flex items-center justify-center rounded p-1 text-zinc-500 transition-colors hover:bg-zinc-800 hover:text-zinc-300"
        title="Insert emoji"
      >
        <Smile className="h-3.5 w-3.5" />
      </button>

      {open && (
        <div className="absolute bottom-7 right-0 z-50 w-72 rounded-xl border border-zinc-700 bg-zinc-900 shadow-xl">
          {/* Search */}
          <div className="px-2 pt-2">
            <input
              type="text"
              value={search}
              onMouseDown={(e) => e.stopPropagation()}
              onChange={(e) => setSearch(e.target.value)}
              placeholder="Search categoriesвҖҰ"
              className="w-full rounded-md border border-zinc-700 bg-zinc-800 px-2 py-1 text-xs text-zinc-200 placeholder:text-zinc-600 outline-none focus:border-zinc-500"
            />
          </div>

          {/* Category tabs */}
          {!search.trim() && (
            <div className="flex gap-0.5 overflow-x-auto border-b border-zinc-800 px-2 pt-2 scrollbar-none">
              {EMOJI_CATEGORIES.map((cat, i) => (
                <button
                  key={cat.label}
                  type="button"
                  onMouseDown={(e) => { e.preventDefault(); setActiveCategory(i); }}
                  className={`shrink-0 rounded px-2 py-1 text-[10px] font-medium transition-colors ${
                    activeCategory === i
                      ? "bg-zinc-700 text-zinc-100"
                      : "text-zinc-500 hover:text-zinc-300"
                  }`}
                >
                  {cat.label}
                </button>
              ))}
            </div>
          )}

          {/* Emoji grid */}
          <div className="grid grid-cols-9 gap-0.5 overflow-y-auto p-2" style={{ maxHeight: "180px" }}>
            {displayEmojis.map((emoji, idx) => (
              <button
                key={`${emoji}-${idx}`}
                type="button"
                onMouseDown={(e) => {
                  e.preventDefault();
                  onSelect(emoji);
                  setOpen(false);
                }}
                className="flex h-7 w-7 items-center justify-center rounded text-base transition-colors hover:bg-zinc-700"
              >
                {emoji}
              </button>
            ))}
          </div>
        </div>
      )}
    </div>
  );
}
